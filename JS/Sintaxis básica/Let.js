//Si se declara una variable sin asignarle un valor la misma adquiere el valor undefined.
let a;
console.log(a);
//Let se utiliza para declarar variables en JavaScript y opcionalmente asignarles un valor.
a={id :'Let', name:'Let-a'};
console.log(a);
//El valor de una variable puede ser modificado multiples veces pero solo puede declararse una vez dentro del bloque.
a.id='Let-2';
console.log(a);
