//La sentencia switch de JS utiliza evalúa un expresión y la compara con una serie de valores indicadas por las cláusulas case
const a=10;
switch(a){
    case 10:
        console.log("10");
        break;
    case 20:
        console.log("20");
        break;
    case 30:
        console.log("30");
        break;
    default:
        console.log("default");
        break;
}